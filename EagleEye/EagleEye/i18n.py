def set_language(language: str) -> None:
    """Set the active frontend language if it is supported."""
    global _LANGUAGE
    if language in LANGUAGES:
        _LANGUAGE = language


def get_language() -> str:
    """Return the active frontend language code."""
    return _LANGUAGE


def tr(key: str, **kwargs) -> str:
    """Translate a key and format it with keyword arguments."""
    text = _TRANSLATIONS.get(_LANGUAGE, {}).get(
        key, _TRANSLATIONS[DEFAULT_LANGUAGE].get(key, key))
    return text.format(**kwargs) if kwargs else text


def language_name(language: str) -> str:
    """Return the localized display name for a language code."""
    name_keys = {
        "en": "language.english",
        "de": "language.german",
    }
    return tr(name_keys.get(language, language))


def translate_status(status: str) -> str:
    """Translate internal worker status labels for display."""
    key = _STATUS_KEYS.get(status)
    return tr(key) if key else status
